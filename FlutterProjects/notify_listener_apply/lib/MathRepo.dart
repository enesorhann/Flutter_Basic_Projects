class Mathrepo{
  int  topla(String alinanSayi1,String alinanSayi2){
    int num1 = int.parse(alinanSayi1);
    int num2 = int.parse(alinanSayi2);
    int res = num1 + num2;
    return res;
  }
  int  carp(String alinanSayi1,String alinanSayi2){
    int num1 = int.parse(alinanSayi1);
    int num2 = int.parse(alinanSayi2);
    int res = num1 * num2;
    return res;
  }
}